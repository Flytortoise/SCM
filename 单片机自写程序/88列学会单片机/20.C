//完成定时器T0为延时实现LED闪烁的功能
#include<reg52.h>
void main()
{
	EA = 1;
	ET0 = 1;
	TMOD = 0X01;
	TH0 = (65536-46083)/256;
	TL0 = (65536-46083)%256;
	TR0 = 1;
	TF0 = 0;	//查询方式软件清0；中断方式时硬件自动清0
	P1 = 0xff;
	while(1)
	{
		while(TF0 == 0)
			;
		TF0 = 0;
		P1 = ~P1;
		TH0 = (65536-46083)/256;
		TL0 = (65536-46083)%256;
	}
}
